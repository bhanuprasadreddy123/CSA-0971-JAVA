class TechNumberCheck {
    public static void main(String[] args) {
        int n = 3025;
        int a = 30, b = 25;
        int c = (a + b) * 2;

        if (c == n) {
            System.out.println(n + " is a tech number.");
        } else {
            System.out.println(n + " is not a tech number.");
        }
    }
}
